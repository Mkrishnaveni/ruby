
# Ruby program to demonstrate  
# the Logical Operators 
   
# Variables 
a = 10
b = 20
c = 30
  
# using && operator 
if a == 10 && b == 20 && c == 30
    puts "Logical AND Operator"
    puts result = a * b * c 
end
  
# using || operator 
puts "Logical OR operator"
if a == 10 || b == 20
    puts result = a + b + c 
end
  
# using ! operator 
puts "Logical Not Operator"
puts !(true) 
     
